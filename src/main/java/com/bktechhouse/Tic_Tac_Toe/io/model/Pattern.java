package com.bktechhouse.Tic_Tac_Toe.io.model;

public class Pattern {
    int row;
    int col;

    public Pattern(int row, int col) {
        this.row = row;
        this.col = col;
    }

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public int getCol() {
        return col;
    }

    public void setCol(int col) {
        this.col = col;
    }
}
